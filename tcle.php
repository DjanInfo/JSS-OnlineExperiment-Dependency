Este questionário é direcionado a profissionais da área de TI, estudantes, professores e pesquisadores que tenham conhecimento básico na linguagem C e em diretivas do tipo #ifdefs.</p>

<p>1.    Apresentação da pesquisa:Os sistemas configuráveis são constituídos de diversas funcionalidades (features) que podem ser habilitadas ou desabilitadas permitindo variabilidade. Uma das técnicas mais
utilizadas para promover a variabilidade é a compilação condicional. A linguagem de programação C implementa compilação condicional nativamente através da diretiva de
pré-processamento #ifdef. Esta técnica permite que trechos do código fonte de features delimitados por #ifdefs sejam compilados ou não. 
Duas ou mais fatures podem facilmente compartilhar elementos de código fonte. Quando diferentes features referem-se ao mesmo elemento de programa, como por exemplo uma variável, nós temos a ocorrência de uma dependência entre features.</p>

<p>Justificativa: O desenvolvedor ao depurar programas com
dependência entre features pode ter a compreensibilidade afetada, pois o desenvolvedor
precisa analisar diversos cenários que afetam a mesma variável. O esforço para entender
as configurações que contêm dependências entre features pode ser ainda mais alto do
que as sem dependências.</p>

<p>2.    Objetivos da pesquisa: O objetivo deste trabalho é usar diferentes estudos experimentais, a fim de compreender quantitativamente e qualitativamente o impacto das
dependências entre features na compreensibilidade de sistemas configuráveis que utilizam diretivas de pré-processamento #ifdef.</p>

